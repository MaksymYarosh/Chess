public class Player {

    public String firstName;
    String lastName;
    int age;
    int rating;
    int points;

    Player() {
    }

    Player(String firstName, int age, int rating, int points) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.rating = rating;
        this.points = points;
    }
}
